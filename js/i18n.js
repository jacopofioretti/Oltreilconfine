/* ─── Core i18n engine ─── */
const I18n = (() => {
  const STORAGE_KEY = 'oc_lang';
  let current = localStorage.getItem(STORAGE_KEY) || 'it';

  function t(key) {
    const dict = translations[current] || translations['it'];
    return dict[key] !== undefined ? dict[key] : (translations['it'][key] || key);
  }

  function applyAll() {
    document.documentElement.lang = current;

    /* text content */
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      el.innerHTML = t(key);
    });

    /* placeholders */
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.placeholder = t(el.dataset.i18nPh);
    });

    /* aria-labels */
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      el.setAttribute('aria-label', t(el.dataset.i18nAria));
    });

    /* lang buttons */
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === current);
    });

    /* loader logo */
    const loaderLogo = document.querySelector('.loader-logo');
    if (loaderLogo) loaderLogo.textContent = current === 'sl' ? 'Prek Meje' : 'Oltreconfine';
  }

  function setLang(lang) {
    if (!translations[lang]) return;
    current = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    applyAll();
    /* dispatch event so other scripts can react */
    document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
  }

  function getLang() { return current; }

  /* auto-init on DOMContentLoaded */
  document.addEventListener('DOMContentLoaded', applyAll);

  return { t, setLang, getLang, applyAll };
})();

window.I18n = I18n;
window.setLang = I18n.setLang;
